-- Задание: Выгрузите данные о пользователях из Канады и разделите их на три группы в зависимости от количества просмотров их профилей.
-- Пользователям с количеством просмотров:
-- - больше либо равно 350 присвойте группу 1;
-- - меньше 350, но больше либо равно 100 — группу 2;
-- - меньше 100 — группу 3.
-- Пользователи с количеством просмотров меньше либо равным нулю не должны попасть в итоговую таблицу.

-- Основной запрос для получения идентификатора пользователя, количества просмотров и группы
SELECT
    id,                                      -- Идентификатор пользователя
    views,                                   -- Количество просмотров профиля
    CASE
        -- Определение группы по количеству просмотров
        WHEN views >= 350 THEN 1            -- Группа 1: просмотры >= 350
        WHEN views >= 100 THEN 2            -- Группа 2: 100 <= просмотры < 350
        ELSE 3                              -- Группа 3: просмотры < 100
    END AS group
FROM stackoverflow.users
-- Фильтрация пользователей по местоположению и количеству просмотров
WHERE location LIKE '%Canada%' AND views > 0;
